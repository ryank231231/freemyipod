#include "global.h"
#include "s5l8701.h"


void clockgate_enable(int gate, bool enable)
{
    if (enable) PWRCON(gate >> 5) &= ~(1 << (gate & 0x1f));
    else PWRCON(gate >> 5) |= 1 << (gate & 0x1f);
}
